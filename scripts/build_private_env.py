"""
Build SWE-Smith Docker environment for a private repository.

SWE-smith's standard pipeline requires:
  1. A registered profile class in swesmith.profiles
  2. A mirror repo in the swesmith GitHub org
  3. A conda environment.yml generated by try_install_py

For private repos, none of these exist. This script:
  1. Dynamically creates and registers a PythonProfile subclass
  2. Creates a mirror in your own GitHub org (via gh CLI)
  3. Generates a minimal conda env file from the repo's requirements
  4. Builds the Docker image using HTTPS + gh auth token (no SSH needed)

Prerequisites:
  - gh CLI authenticated (gh auth status)
  - Docker running with BuildKit enabled

Usage:
  python scripts/build_private_env.py [--force] [--python-version 3.12]
"""

import argparse
import subprocess
import sys
from dataclasses import dataclass, field
from conf import get as _conf


def _gh(cmd: str, check: bool = True) -> subprocess.CompletedProcess:
    """Run a gh CLI command and return the result."""
    return subprocess.run(
        f"gh {cmd}", shell=True, capture_output=True, text=True, check=check,
    )


def _gh_token() -> str:
    """Get the current gh auth token."""
    result = _gh("auth token", check=False)
    if result.returncode != 0 or not result.stdout.strip():
        print("ERROR: Cannot get gh auth token. Run: gh auth login")
        sys.exit(1)
    return result.stdout.strip()



def main():
    parser = argparse.ArgumentParser(description="Build Docker env for private repo")
    parser.add_argument("--force", "-f", action="store_true", help="Force rebuild")
    parser.add_argument("--python-version", default=_conf("PYTHON_VERSION", "3.12"))
    parser.add_argument("--owner", default=_conf("REPO_OWNER"))
    parser.add_argument("--repo", default=_conf("REPO_NAME"))
    parser.add_argument("--commit", default=None, help="Commit hash (default: latest on main)")
    parser.add_argument("--install-cmd", default=None, help="Custom install command")
    args = parser.parse_args()

    # Verify gh is authenticated
    gh_check = _gh("auth status", check=False)
    if gh_check.returncode != 0:
        print("ERROR: gh CLI not authenticated.")
        print("  Run: gh auth login")
        sys.exit(1)

    # Resolve commit using gh
    if args.commit is None:
        result = _gh(f"api repos/{args.owner}/{args.repo}/commits/main --jq .sha", check=False)
        if result.returncode == 0 and result.stdout.strip():
            args.commit = result.stdout.strip()
        else:
            print(f"ERROR: Cannot resolve latest commit for {args.owner}/{args.repo}")
            print(f"  gh error: {result.stderr.strip()}")
            sys.exit(1)
        print(f"Using latest main commit: {args.commit[:8]}")

    # Check Docker
    try:
        subprocess.run("docker ps", shell=True, check=True, capture_output=True)
    except Exception:
        print("ERROR: Docker is not running.")
        sys.exit(1)

    # Import swesmith components
    from swesmith.profiles.base import registry
    from swesmith.profiles.python import PythonProfile

    # Create custom profile class
    @dataclass
    class PrivateRepoProfile(PythonProfile):
        owner: str = args.owner
        repo: str = args.repo
        commit: str = args.commit
        python_version: str = args.python_version
        install_cmds: list[str] = field(default_factory=lambda: [
            "python -m pip install -e '.[dev]'" if args.install_cmd is None else args.install_cmd,
        ])

    # Register it
    registry.register_profile(PrivateRepoProfile)
    profile = PrivateRepoProfile()

    print(f"Profile registered:")
    print(f"  Image name: {profile.image_name}")
    print(f"  Repo:       {args.owner}/{args.repo}")
    print(f"  Commit:     {args.commit[:8]}")
    print(f"  Python:     {args.python_version}")
    print()

    # Check if image already exists
    if not args.force:
        try:
            subprocess.run(
                f"docker image inspect {profile.image_name}",
                shell=True, check=True, capture_output=True,
            )
            print(f"Image already exists: {profile.image_name}")
            print("Use --force to rebuild.")
            return
        except subprocess.CalledProcessError:
            pass

    # Build Docker image directly (skip mirror, skip conda — use HTTPS + gh token)
    print("=== Building Docker image ===")
    _build_image_https(profile, args)

    print()
    print("=== Done ===")
    print(f"Verify: docker images | grep {args.repo.lower()}")
    print(f"Test:   docker run -it --rm {profile.image_name} bash")


def _build_image_https(profile, args):
    """Build Docker image using HTTPS clone with gh auth token.

    No SSH keys or agents needed — uses `gh auth token` to authenticate
    the git clone inside the Docker build via a build secret.
    """
    from swesmith.constants import LOG_DIR_ENV

    env_dir = LOG_DIR_ENV / profile.repo_name
    env_dir.mkdir(parents=True, exist_ok=True)

    # Get token from gh CLI
    token = _gh_token()

    # Plain URL to reset remote after clone (so token isn't stored)
    clone_url_plain = f"https://github.com/{args.owner}/{args.repo}.git"

    # Try to get the base image
    base_image = "swebench/swesmith.x86_64"
    try:
        subprocess.run(
            f"docker image inspect {base_image}",
            shell=True, check=True, capture_output=True,
        )
    except Exception:
        try:
            print(f"Pulling base image {base_image}...")
            subprocess.run(f"docker pull {base_image}", shell=True, check=True)
        except Exception:
            print(f"Cannot pull {base_image}, using python:3.12-slim")
            base_image = "python:3.12-slim"

    # Use a build secret for the token so it doesn't leak into image layers
    dockerfile = f"""# syntax=docker/dockerfile:1
FROM {base_image}

# Install system dependencies
RUN apt-get update && apt-get install -y \\
    git curl build-essential && \\
    rm -rf /var/lib/apt/lists/*

# Clone the private repository using token from build secret
RUN --mount=type=secret,id=gh_token \\
    GH_TOKEN=$(cat /run/secrets/gh_token) && \\
    git clone https://x-access-token:${{GH_TOKEN}}@github.com/{args.owner}/{args.repo}.git /testbed && \\
    cd /testbed && \\
    git checkout {args.commit} && \\
    git remote set-url origin {clone_url_plain}

WORKDIR /testbed

# Install Python dependencies
RUN pip install --no-cache-dir -e ".[dev]" 2>/dev/null || \\
    pip install --no-cache-dir -e . || \\
    echo "WARNING: pip install failed, continuing anyway"

# Install pytest if not already installed
RUN pip install --no-cache-dir pytest 2>/dev/null || true

# Ensure git is configured for swesmith
RUN cd /testbed && \\
    git config user.name "swesmith" && \\
    git config user.email "swesmith@anon.com"
"""

    dockerfile_path = env_dir / "Dockerfile"
    with open(dockerfile_path, "w") as f:
        f.write(dockerfile)

    # Write token to a temp file for Docker build secret
    token_file = env_dir / ".gh_token"
    token_file.write_text(token)

    print(f"Dockerfile written to {dockerfile_path}")
    print(f"Building image: {profile.image_name}")
    print()

    build_cmd = (
        f"DOCKER_BUILDKIT=1 docker build"
        f" -f {dockerfile_path}"
        f" --platform linux/x86_64"
        f" --no-cache"
        f" --secret id=gh_token,src={token_file}"
        f" -t {profile.image_name}"
        f" {env_dir}"
    )

    try:
        result = subprocess.run(build_cmd, shell=True)
        if result.returncode != 0:
            print("\nDocker build failed.")
            print("\nTroubleshooting:")
            print("  1. Check Docker is running: docker ps")
            print("  2. Check gh auth: gh auth status")
            print(f"  3. Check repo access: gh repo view {args.owner}/{args.repo}")
            sys.exit(1)

        print(f"\nImage built: {profile.image_name}")
    finally:
        # Always clean up the token file
        if token_file.exists():
            token_file.unlink()


if __name__ == "__main__":
    main()
