"""
Monkey-patch gepa's gskill data loader to support local task instances.

When tasks have been generated locally via SWE-smith (steps 1-3), this patch
makes `train_optimize_anything` load from the local JSON file instead of
trying to download from HuggingFace (where private repos won't exist).

Usage:
    python scripts/local_data_patch.py [same args as train_optimize_anything]

This script:
  1. Checks for local validated tasks at logs/valid_instances/<repo>.json
  2. If found, patches load_and_split_data to use local data
  3. Delegates to train_optimize_anything.main()
"""

import json
import random
import sys
from pathlib import Path


def load_local_data(repo: str, train_size: int, val_size: int, test_size: int, seed: int = 42):
    """Load task instances from local JSON (generated by SWE-smith steps 1-3)."""
    # Try multiple possible locations
    candidates = [
        Path(f"logs/valid_instances/{repo}.json"),
        Path(f"../logs/valid_instances/{repo}.json"),
        Path(f"swe-gym/logs/valid_instances/{repo}.json"),
    ]

    data_path = None
    for p in candidates:
        if p.exists():
            data_path = p
            break

    if data_path is None:
        print(f"ERROR: No local task data found for {repo}")
        print(f"Looked in: {[str(p) for p in candidates]}")
        print("Run steps 1-3 first: make build-env && make generate-tasks && make validate-tasks")
        sys.exit(1)

    total_needed = train_size + val_size + test_size
    print(f"Loading {repo} data from local file: {data_path} (need {total_needed})...")

    with open(data_path) as f:
        all_data = json.load(f)

    # Handle both list and dict formats
    if isinstance(all_data, dict):
        all_data = list(all_data.values())

    print(f"Loaded {len(all_data)} local task instances")

    # Generate problem_statement for SWE-smith instances that lack one
    for item in all_data:
        if "problem_statement" not in item or not item["problem_statement"].strip():
            f2p = item.get("FAIL_TO_PASS", [])
            patch = item.get("patch", "")
            # Extract changed files from patch
            files = [
                line.split(" b/")[-1]
                for line in patch.split("\n")
                if line.startswith("diff --git")
            ]
            files_str = ", ".join(files) if files else "unknown files"
            tests_str = "\n".join(f"- {t}" for t in f2p) if f2p else "- (unknown tests)"
            item["problem_statement"] = (
                f"There is a bug in {files_str} that causes the following test(s) to fail:\n"
                f"{tests_str}\n\n"
                f"Please investigate the failing tests and fix the underlying issue."
            )


    # Shuffle with seed
    random.seed(seed)
    random.shuffle(all_data)

    # Scale down if not enough data
    if len(all_data) < total_needed:
        print(f"WARNING: Only {len(all_data)} examples available, need {total_needed}")
        ratio = len(all_data) / total_needed if total_needed > 0 else 0
        train_size = max(1, int(train_size * ratio))
        val_size = max(1, int(val_size * ratio))
        test_size = max(1, len(all_data) - train_size - val_size)

    train_data = all_data[:train_size]
    val_data = all_data[train_size:train_size + val_size]
    test_data = all_data[train_size + val_size:train_size + val_size + test_size]

    print(f"Split: {len(train_data)} train, {len(val_data)} val, {len(test_data)} test")
    return train_data, val_data, test_data


def has_local_data(repo: str) -> bool:
    """Check if local task data exists for this repo."""
    return any(
        Path(p).exists()
        for p in [
            f"logs/valid_instances/{repo}.json",
            f"../logs/valid_instances/{repo}.json",
            f"swe-gym/logs/valid_instances/{repo}.json",
        ]
    )


if __name__ == "__main__":
    # Register private repo profile so swesmith's registry can find it
    import register_profile
    register_profile.ensure_profile_registered()

    # Patch out hardcoded us.api.openai.com regional endpoint in swe_harness.
    # GEPA hardcodes api_base="https://us.api.openai.com/v1" for OpenAI models
    # which fails if the API key doesn't support regional endpoints.
    from minisweagent.models.litellm_model import LitellmModel, litellm
    _orig_litellm_completion = litellm.completion
    def _patched_completion(*args, **kwargs):
        api_base = kwargs.get("api_base", "")
        if api_base and "us.api.openai.com" in api_base:
            kwargs["api_base"] = "https://api.openai.com/v1"
        return _orig_litellm_completion(*args, **kwargs)
    litellm.completion = _patched_completion
    print("[patch] Redirected us.api.openai.com -> api.openai.com")


    # Detect repo from args (default from repo.conf)
    from conf import get as _conf
    repo = _conf("REPO_KEY")
    for i, arg in enumerate(sys.argv):
        if arg == "--repo" and i + 1 < len(sys.argv):
            repo = sys.argv[i + 1]

    # Patch if local data exists, otherwise fall through to HF
    if has_local_data(repo):
        import gepa.gskill.gskill.train_optimize_anything as toa
        toa.load_and_split_data = load_local_data
        print(f"[patch] Using local task data for {repo}")
    else:
        print(f"[patch] No local data for {repo}, falling back to HuggingFace")

    # Run the real main()
    from gepa.gskill.gskill.train_optimize_anything import main
    main()
